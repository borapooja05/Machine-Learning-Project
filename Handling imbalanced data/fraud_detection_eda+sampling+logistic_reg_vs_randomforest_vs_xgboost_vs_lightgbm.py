def RandomSearchCV_best_param(estimator,param_dict,trainDf, subset_size=50000):
  # Use a smaller subset of the data for faster tuning
  if len(trainDf) > subset_size:
      trainDf_subset = trainDf.sample(n=subset_size, random_state=42)
  else:
      trainDf_subset = trainDf

  # Reduced cv from 5 to 3 for faster execution
  skf = StratifiedKFold(n_splits=3, shuffle=True, random_state=42)

  if estimator=='LogisticRegression':
    LR=LogisticRegression(random_state=42)
    rscv=RandomizedSearchCV(LR,param_dict,cv=skf,scoring='f1_macro',n_jobs=-1,random_state=42)

  elif estimator=='RandomForest':
    RF = RandomForestClassifier(random_state=42)
    rscv=RandomizedSearchCV(RF,param_dict,cv=skf,scoring='f1_macro',n_jobs=-1,random_state=42)

  elif estimator=='XGBoost':
    XGB=XGBoost(random_state=42)
    rscv=RandomizedSearchCV(XGB,param_dict,n_iter=30,cv=skf,scoring='f1_macro',n_jobs=-1,random_state=42)

