def objective_compare_models(trial):
    classifier_name = trial.suggest_categorical("classifier", ["LGBM", "XGBoost", "RandomForest", "LogisticRegression"])

    if classifier_name == "LGBM":
        params = {
            'num_leaves': trial.suggest_int('num_leaves', 15, 256),
            'max_depth': trial.suggest_int('max_depth', 4, 50),
            'learning_rate': trial.suggest_float('learning_rate', 0.005, 0.3, log=True),
            'n_estimators': trial.suggest_int('n_estimators', 30, 1000),
            'min_child_samples': trial.suggest_int('min_child_samples', 5, 100),
            'subsample': trial.suggest_float('subsample', 0.5, 1.0),
            'colsample_bytree': trial.suggest_float('colsample_bytree', 0.5, 1.0),
            'class_weight': 'balanced',
            'random_state': 42,
            'n_jobs': -1
        }
        classifier = LGBMClassifier(**params)
    elif classifier_name == "XGBoost":
         params = {
            'n_estimators': trial.suggest_int('n_estimators', 50, 1500),
            'max_depth': trial.suggest_int('max_depth', 3, 20),
            'learning_rate': trial.suggest_float('learning_rate', 0.005, 0.3, log=True),
            'gamma': trial.suggest_float('gamma', 0, 1),
            'reg_alpha': trial.suggest_float('reg_alpha', 0, 1),
            'reg_lambda': trial.suggest_float('reg_lambda', 0, 1),
            'min_child_weight': trial.suggest_int('min_child_weight', 1, 10),
            'subsample': trial.suggest_float('subsample', 0.5, 1.0),
            'colsample_bytree': trial.suggest_float('colsample_bytree', 0.5, 1.0),
            'scale_pos_weight': trial.suggest_float('scale_pos_weight', 1, 500),
            'objective': 'binary:logistic',
            'seed': 42,
            'n_jobs': -1
        }
         classifier = XGBClassifier(**params)
    elif classifier_name == "RandomForest":
        params = {
            'n_estimators': trial.suggest_int('n_estimators', 50, 1500),
            'max_depth': trial.suggest_int('max_depth', 3, 20),
            'min_samples_split': trial.suggest_int('min_samples_split', 2, 10),
            'min_samples_leaf': trial.suggest_int('min_samples_leaf', 1, 10),
            'max_features': trial.suggest_categorical('max_features', ['sqrt', 'log2', None]),
            'class_weight': 'balanced',
            'random_state': 42,
            'n_jobs': -1
        }
        classifier = RandomForestClassifier(**params)
    else: # LogisticRegression
        params = {
            'C': trial.suggest_float('C', 1e-4, 1e4, log=True),
            'penalty': trial.suggest_categorical('penalty', ['l1', 'l2']),
            'solver': trial.suggest_categorical('solver', ['liblinear', 'lbfgs']), # lbfgs only supports l2
            'class_weight': 'balanced',
            'random_state': 42,
            'n_jobs': -1
        }
        # Handle solver compatibility with penalty
        if params['penalty'] == 'l1' and params['solver'] == 'lbfgs':
             params['solver'] = 'liblinear' # lbfgs doesn't support l1
        elif params['penalty'] == 'elasticnet':
             params['solver'] = 'saga' # saga supports elasticnet

        classifier = LogisticRegression(**params)

    # Use StratifiedKFold for cross-validation
    skf = StratifiedKFold(n_splits=3, shuffle=True, random_state=42)
    f1_macro_scores = []

    # Use a pipeline to scale data within each fold for Logistic Regression
    if classifier_name == "LogisticRegression":
        pipeline = Pipeline([
            ('scaler', StandardScaler()),
            ('classifier', classifier)
        ])
    else:
        pipeline = classifier

    for train_idx, test_idx in skf.split(X_train, y_train):
        X_train_fold, X_test_fold = X_train.iloc[train_idx], X_train.iloc[test_idx]
        y_train_fold, y_test_fold = y_train.iloc[train_idx], y_train.iloc[test_idx]

        pipeline.fit(X_train_fold, y_train_fold)
        y_pred = pipeline.predict(X_test_fold)
        f1_macro_scores.append(f1_score(y_test_fold, y_pred, average='macro'))

    return np.mean(f1_macro_scores)

